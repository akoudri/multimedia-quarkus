package com.akfc.users.data;

/**
 * Enumeration of resource types that users can subscribe to.
 *
 * Mirrors the resource types from the catalog service.
 */
public enum ResourceType {
    /**
     * Book resource type.
     */
    BOOK,

    /**
     * Movie resource type.
     */
    MOVIE,

    /**
     * Music/Album resource type.
     */
    MUSIC
}
